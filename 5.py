s_1 = input()
s_2 = input()
s_3 = input()

ch_1 = set(s_1)
ch_2 = set(s_2)
ch_3 = set(s_3)
res = set()

for ch in ch_1:
    if ch not in ch_2 and ch not in ch_3:
        res.add(ch)
for ch in ch_2:
    if ch not in ch_1 and ch not in ch_3:
        res.add(ch)
for ch in ch_3:
    if ch not in ch_1 and ch not in ch_2:
        res.add(ch)

print(' '.join(res))
